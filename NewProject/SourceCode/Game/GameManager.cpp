//=================================================================
// [ スコア増加関数 ]
//=================================================================
void GameManager::AddScore(int num)
{
	score_ += num;

}

//=================================================================
// [ スコア取得関数 ]
//=================================================================
int GameManager::GetScore()
{
	if (score_ <= SCORE_MIN)
		score_ = SCORE_MIN;

	return score_;
}

//=================================================================
// [ ライフダメージ関数 ]
//=================================================================
void GameManager::DamageLife(int num)
{
	player_life_ -= num;
}

//=================================================================
// [ プレイヤーライフ取得関数 ]
//=================================================================
int GameManager::GetPlayerLife()
{
	return player_life_;
}